export const getCareerExams = (careerId: string) => {
  const mappings = careerExamMappings.filter((mapping) => mapping.careerId === careerId)
  return mappings
    .map((mapping) => ({
      exam: exams.find((exam) => exam.id === mapping.examId),
      isEligible: mapping.isEligible,
    }))
    .filter((item): item is { exam: Exam; isEligible: boolean } => Boolean(item.exam))
}

export const getRelatedCareers = (career: Career) =>
  career.relatedCareerIds
    .map((id) => careers.find((item) => item.id === id))
    .filter((item): item is Career => Boolean(item))

